import numpy as np
import matplotlib.pyplot as plt

# Parametri fissi
LM_fixed = 4  # dB
pout_targets = [0.25, 0.10, 0.05, 0.01]
labels = ['25%', '10%', '5%', '1%']

# Valori di guadagno da testare
G_dB_values = np.arange(110, 141, 5)  # 110, 115, 120, 125, 130, 135, 140

def K_from_margin(M_dB):
    return (np.log(10) / 10) * M_dB

def pout(sigma, G, K):
    x = K / (2 * sigma**2 * G)
    return (1 + x) * np.exp(-x)

def bisection(G, M_dB, pout_target, tol=1e-15):
    K = K_from_margin(M_dB)
    a, b = 1e-9, 1e-3
    for _ in range(100):
        mid = (a + b) / 2
        if pout(mid, G, K) > pout_target:
            b = mid
        else:
            a = mid
        if (b - a) < tol:
            break
    return (a + b) / 2

# Stampa tabella
print(f"{'G [dB]':<10}", end='')
for p in labels:
    print(f"{p:>12}", end='')
print()

for G_dB in G_dB_values:
    G = 10**(G_dB / 10)
    print(f"{G_dB:<10.2f}", end='')
    for p in pout_targets:
        s = bisection(G, LM_fixed, p) * 1e6
        print(f"{s:>12.3f}", end='')
    print()

# Grafico
plt.figure(figsize=(10, 6))
for p, lbl in zip(pout_targets, labels):
    sigma_vals = [bisection(10**(G_dB/10), LM_fixed, p) * 1e6
                  for G_dB in G_dB_values]
    plt.plot(G_dB_values, sigma_vals, marker='o', label=f'Pout = {lbl}')

plt.xlabel('Guadagno d\'antenna G [dB]')
plt.ylabel(r'$\sigma_{\theta,\max}$ [$\mu$rad]')
plt.legend(frameon=False)
plt.grid(True, linestyle='--', alpha=0.6)
plt.tight_layout()
plt.savefig('sigma_max_vs_gain.png', dpi=300, bbox_inches='tight')
plt.show()